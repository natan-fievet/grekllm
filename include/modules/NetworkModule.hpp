///////////////////////////////////////////////////////////////////////////////
// Dependencies
///////////////////////////////////////////////////////////////////////////////
#include "IMonitorModule.hpp"
#include <list>

///////////////////////////////////////////////////////////////////////////////
/// \brief Network's related module
///
///////////////////////////////////////////////////////////////////////////////
class NetworkModule : public IMonitorModule
{
public:
    ///////////////////////////////////////////////////////////////////////////
    // Custom type
    ///////////////////////////////////////////////////////////////////////////
    using Data = struct { int up, down; };

private:
    ///////////////////////////////////////////////////////////////////////////
    // Private properties
    ///////////////////////////////////////////////////////////////////////////
    int m_up;                   //<! Network up value
    int m_down;                 //<! Network down value
    bool m_enabled;             //<! Module enabled status
    std::list<Data> m_graph;    //<! Module graph data

public:
    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
    NetworkModule(void);

    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
    ~NetworkModule();

public:
    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
    void setEnabled(bool enabled) override;

    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
    bool isEnabled(void) const override;

    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
    bool refresh(void) override;

    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
    void setUp(int up);

    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
    void setDown(int down);

    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
    int getUp(void) const;

    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
    int getDown(void) const;

    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
    std::list<Data> getGraph(void) const;
};
